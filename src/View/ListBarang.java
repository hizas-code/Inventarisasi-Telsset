    public void setTableData(ArrayList<Objects> objects){
        int x = 0;
        resetTable();
        for(Objects o : objects){
            tableObject.setValueAt(o.getName(), x, 0);
            tableObject.setValueAt(o.statusToString(), x, 1);
            tableObject.setValueAt(o.categoryToString(), x, 2);
            x++;
        }
    }
    
    public void resetTable(){
        DefaultTableModel dm = (DefaultTableModel)tableObject.getModel();
        dm.setRowCount(0);
        dm.setRowCount(100);
    }
